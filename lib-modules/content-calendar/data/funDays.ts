// Получить "забавный день" по дате (формат YYYY-MM-DD)
export function getFunDayForDate(dateString: string): FunDay | undefined {
  const date = new Date(dateString)
  const monthDay = `${String(date.getMonth() + 1).padStart(2, '0')}-${String(date.getDate()).padStart(2, '0')}`
  return funDays.find(day => day.date === monthDay)
}
